def generate_inspiration(style_name):
  data = styles[style_name]
  theme = random.choice(data["themes"])
  idea = f"A painting expressing '{theme}' in the {style_name} style."

  return {
      "idea": idea,
      "technique": random.choice(data["technique"].split(";")).strip(),
      "colors": data["colors"],
      "example": random.choice(data["examples"]),
      "artist": random.choice(data["artists"])

}

